package utils

import (
	"fmt"
	"io"
	"strings"
)

// Confirm 确认
func Confirm(msg string, reader io.Reader, writer io.Writer) bool {
	fmt.Fprintf(writer, "%s ", msg)

	var response string
	_, err := fmt.Fscanln(reader, &response)
	if err != nil {
		return false
	}
	response = strings.ToUpper(response)
	if response == "Y" || response == "YES" {
		return true
	}
	return false
}
