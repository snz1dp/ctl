package utils

import (
	"github.com/pkg/errors"
	"os/exec"
	"runtime"
)

// OpenBrowser -
func OpenBrowser(url string) (err error) {

	switch runtime.GOOS {
	case "linux":
		err = exec.Command("xdg-open", url).Start()
	case "windows":
		err = exec.Command("rundll32", "url.dll,FileProtocolHandler", url).Start()
	case "darwin":
		err = exec.Command("open", url).Start()
	default:
		err = errors.Errorf("Unsupported platform %q; open %s in your browser.\n", runtime.GOOS, url)
	}
	return
}
